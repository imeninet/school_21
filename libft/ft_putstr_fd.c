#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int num;

	num = 0;
	if (s)
	{
		while (s[num] != '\0')
		{
			ft_putchar_fd(s[num], fd);
			num++;
		}
	}
}
